// Helper functions
export function getCoopsByState(state: string): CoOp[] {
  return coops.filter((c) => c.state === state);
}

export function getCoopsByCounty(state: string, county: string): CoOp[] {
  return coops.filter((c) => c.state === state && c.county === county);
}

export function getCoopsByType(type: CoOp["type"]): CoOp[] {
  return coops.filter((c) => c.type === type);
}

export function getUniqueCounties(state: string): string[] {
  return Array.from(
    new Set(coops.filter((c) => c.state === state).map((c) => c.county))
  ).sort();
}

export function getUniqueStates(): string[] {
  return Array.from(new Set(coops.map((c) => c.state))).sort();
}
